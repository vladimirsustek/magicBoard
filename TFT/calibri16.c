uint32_t sPrintCalibri16(uint32_t xOffset, uint32_t yOffset, uint32_t idx, uint32_t color)
{

	if(' ' - 33 == idx)
	{
		  return 8;
	}

	uint32_t adrOffset = calibri_16ptDescriptors[idx].offset;
	uint32_t width = calibri_16ptDescriptors[idx].width;

	/* Get align width in bytes*/
	width = (width % 8) ? width / 8 + 1 : width / 8;

	uint32_t height = (calibri_16ptDescriptors[idx + 1].offset - adrOffset)/width;
	uint8_t inspectedByte = 0;
	uint8_t inspectedBit = 0;

	  for(uint32_t line = 0; line < height; line++)
	  {
		  for(uint32_t pixel = 0; pixel < width*8; pixel++)
		  {
			  if(pixel % 8 == 0)
			  {
				  inspectedByte = calibri_16ptBitmaps[adrOffset + line* width + pixel / 8];
			  }

			  inspectedBit = inspectedByte & (1 << (7 - (pixel % 8)));
			  inspectedBit = inspectedBit >> (7 - (pixel % 8));

			  if(inspectedBit)
			  {
				  drawPixel(height - line + yOffset, pixel + xOffset, color);
			  }
		  }
	  }

	  return calibri_16ptDescriptors[idx].width + 2;
}

